public class Main
{
    public static void main(String[] args)
    {
        int numToExamine = 2;
        System.out.println("Number: " + numToExamine);
        if (numToExamine % 2 == 0)
        {
            System.out.println("Number is Even!");
        } else 
        {
            System.out.println("Number is Odd!");
        }
        numToExamine = 3;
        System.out.println("Number: " + numToExamine);
        if (numToExamine % 2 == 0)
        {
            System.out.println("Number is Even!");
        } else 
        {
            System.out.println("Number is Odd!");
        }
    }
}